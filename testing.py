# Example variables
outputx = 1.2
outputy = 3.4
outputz = 5.6

# Create a tuple of integers
coordinate_tuple = (int(outputx), int(outputy), int(outputz))

# Access each value by index
value_x = coordinate_tuple[0]
value_y = coordinate_tuple[1]
value_z = coordinate_tuple[2]

print("Value X:", value_x)  # Output: Value X: 1
print("Value Y:", value_y)  # Output: Value Y: 3
print("Value Z:", value_z)  # Output: Value Z: 5
